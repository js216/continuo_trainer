--[[
DESCRIPTION
     stats.lua reads SCORE lines from standard input and maintains a
     persistent log of performance. It calculates daily total scores,
     monitors a daily "goal streak," and applies the SM-2 algorithm
     (Spaced Repetition System) to individual lessons.

INPUT FORMAT
     The program expects lines in the format generated by score.lua:
     SCORE lesson=<id> accuracy=<n> score=<n.nn> ...

OUTPUT FORMAT
     For every valid SCORE line processed, the program writes to stdout:
     STATS total_today=<n.nn> streak=<n> lesson=<id>[ivl=<n>,ease=<n.nn>,n_pass=<n>,n_fail=<n>]

     - total_today: Sum of all scores for the current calendar day.
     - streak: Number of consecutive days where score_goal was reached.
     - ivl: The SRS interval (in days) until the lesson should be reviewed.
     - ease: The SM-2 Easiness Factor (starting at 2.5).
     - n_pass/n_fail: Consecutive counts of success/failure for the lesson.

ERROR HANDLING
     - If no cmdline argument is provided, prints usage and exits with status 1.
     - If the stats file exists but is malformed, prints error and exits.
     - If the stats file cannot be written, prints error and exits.
]]

local DEFAULT_SCORE_GOAL = 1000
local stats_file = arg[1]

if not stats_file then
	print("Usage: lua stats.lua <stats_file.lua>")
	os.exit(1)
end

-------------------------------------------------------------------------------
-- DATA PERSISTENCE
-------------------------------------------------------------------------------

local function load_stats(path)
	local f = io.open(path, "r")
	if not f then
		return {
			score_goal = DEFAULT_SCORE_GOAL,
			daily = {},
			lessons = {},
		}
	end
	f:close()

	local chunk, err = loadfile(path)
	if not chunk then
		io.stderr:write("Error: Could not parse stats file: " .. tostring(err) .. "\n")
		os.exit(1)
	end

	local data = chunk()
	if type(data) ~= "table" or not data.daily or not data.lessons then
		io.stderr:write("Error: Stats file is not in a recognized format.\n")
		os.exit(1)
	end
	return data
end

local function save_stats(path, data)
	local f = io.open(path, "w")
	if not f then
		io.stderr:write("Error: Could not write to stats file.\n")
		os.exit(1)
	end

	local function serialize(t, indent)
		local s = "{\n"
		local next_indent = indent .. "  "
		for k, v in pairs(t) do
			s = s .. next_indent .. "[" .. (type(k) == "string" and string.format("%q", k) or k) .. "] = "
			if type(v) == "table" then
				s = s .. serialize(v, next_indent) .. ",\n"
			elseif type(v) == "string" then
				s = s .. string.format("%q", v) .. ",\n"
			else
				s = s .. tostring(v) .. ",\n"
			end
		end
		return s .. indent .. "}"
	end

	f:write("return " .. serialize(data, "") .. "\n")
	f:flush()
	f:close()
end

-------------------------------------------------------------------------------
-- CORE LOGIC
-------------------------------------------------------------------------------

local function get_date_str()
	return os.date("%Y-%m-%d")
end

local function calculate_streak(data)
	local streak = 0
	local current_ts = os.time()

	while true do
		local d_str = os.date("%Y-%m-%d", current_ts)
		local day_score = data.daily[d_str] or 0

		if day_score >= data.score_goal then
			streak = streak + 1
			current_ts = current_ts - 86400
		else
			-- If we haven't hit the goal today yet, the streak isn't dead;
			-- check if yesterday was successful.
			if d_str == get_date_str() and streak == 0 then
				current_ts = current_ts - 86400
			else
				break
			end
		end
	end
	return streak
end

local function update_anki(l_data, success)
	-- Initialize fields if new lesson
	l_data.n_pass = l_data.n_pass or 0
	l_data.n_fail = l_data.n_fail or 0
	l_data.ivl = l_data.ivl or 0
	l_data.ease = l_data.ease or 2.5

	if success then
		l_data.n_pass = l_data.n_pass + 1
		l_data.n_fail = 0

		-- SM-2 Interval logic based on n_pass
		if l_data.n_pass == 1 then
			l_data.ivl = 1
		elseif l_data.n_pass == 2 then
			l_data.ivl = 6
		else
			l_data.ivl = math.ceil(l_data.ivl * l_data.ease)
		end
		-- Reward 100% accuracy with a slight ease bump
		l_data.ease = l_data.ease + 0.1
	else
		l_data.n_fail = l_data.n_fail + 1
		l_data.n_pass = 0

		-- Penalty: reset interval and reduce ease
		l_data.ivl = 1
		l_data.ease = math.max(1.3, l_data.ease - 0.2)
	end

	l_data.last_date = get_date_str()
end

-------------------------------------------------------------------------------
-- MAIN EXECUTION
-------------------------------------------------------------------------------

local stats = load_stats(stats_file)

for line in io.lines() do
	local time, l_id, acc, score = line:match("SCORE time=(%S+) lesson=(%S+) accuracy=(%S+) score=(%S+)")

	if time and l_id and score then
		local today = get_date_str()
		local score_val = tonumber(score)
		local acc_val = tonumber(acc)
		local lesson_time = tonumber(time)

		-- 1. Update Daily Stats
		stats.daily[today] = (stats.daily[today] or 0) + score_val

		-- 2. Update Lesson Stats
		stats.lessons[l_id] = stats.lessons[l_id] or {}
		update_anki(stats.lessons[l_id], acc_val == 100)

		-- 3. Persist to Disk
		save_stats(stats_file, stats)

		-- 4. Report Progress
		local streak = calculate_streak(stats)
		local l = stats.lessons[l_id]

		print(
			string.format(
				"STATS time=%d total_today=%.2f streak=%d lesson=%s[ivl=%d,ease=%.2f,n_pass=%d,n_fail=%d]",
				lesson_time,
				stats.daily[today],
				streak,
				l_id,
				l.ivl,
				l.ease,
				l.n_pass,
				l.n_fail
			)
		)
	end
end
